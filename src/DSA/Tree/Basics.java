//1️⃣ General Classification of Trees
//General Tree - Each node can have any number of children.
//Binary Tree - Each node has at most two children.
//Ternary Tree - Each node has at most three children.
//N-ary Tree - Each node has at most N children.

//2️⃣ Types of Binary Trees
//1. Full Binary Tree
//Every node has either 0 or 2 children (no node has only one child).
//2. Complete Binary Tree
//All levels except the last are completely filled.
//The last level is filled from left to right.
//3. Perfect Binary Tree
//All levels are completely filled, meaning:
//Each node has exactly 2 children (except leaves).
//The depth is log₂(N + 1) - 1.
//4. Balanced Binary Tree
//The height of the left and right subtrees differs by at most 1.
//Examples: AVL Tree, Red-Black Tree.
//5. Degenerate (Skewed) Tree
//Left-Skewed: Each node has only a left child.
//Right-Skewed: Each node has only a right child.
//It behaves like a linked list.
