package Java_Core.Multithreading._1_Thread;

//A Java Thread is a lightweight subprocess that allows concurrent execution of tasks.
//Threads are used to perform multiple operations simultaneously,
// improving the efficiency of a Java program.

//There are two main ways to create a thread:

//1) Extending the Thread class
//2) Implementing the Runnable interface

//Thread Lifecycle
//A thread goes through different states:
//New – Created but not started (new Thread()).
//Runnable – Ready to run but waiting for CPU time (start() is called).
//Running – Actively executing the run() method.
//Blocked/Waiting – Paused due to synchronization or waiting for a resource.
//Terminated – Execution completed or stopped.

